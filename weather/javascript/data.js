function createNode(_data = null) {
	return { charectors: new Array(27).fill(null), data: _data };
}
function getTries() {
	return JSON.parse(localStorage.getItem("weather-tries-for-search"));
}
function setTries(newTriesForSearch) {
	localStorage.setItem(
		"weather-tries-for-search",
		JSON.stringify(newTriesForSearch)
	);
}
function addOrReturnNode(parentNode, index, node) {
	if (index >= 0 && index < 27 && parentNode.charectors[index] === null) {
		parentNode.charectors[index] = node;
	} else if (node.data && parentNode.charectors[index].data === null) {
		parentNode.charectors[index].data = node.data;
	}

	return parentNode.charectors[index];
}
function updateTries(place) {
	const root = getTries();

	let node = root;
	const capitalPlace = place.place.trim().toUpperCase();

	for (let index = 0; index < capitalPlace.length; index++) {
		let _node;
		const charector = capitalPlace[index];
		const charectorIndex = CHARECTOR_INDEX[charector];

		// last index
		if (index === capitalPlace.length - 1) {
			_node = createNode(place);
		} else {
			_node = createNode();
		}

		node = addOrReturnNode(node, charectorIndex, _node);
	}

	setTries(root);
}
