// date format ---> Monday, 08 May 1995
function formatDate(_date) {
	return _date instanceof Date
		? `${WEEKDAYS[_date.getDay()]}, ${
				_date.getDate() < 10 ? "0" : ""
		  }${_date.getDate()} ${MONTHS[_date.getMonth()].substring(
				0,
				3
		  )} ${_date.getFullYear()}`
		: "";
}
// date duffrence without time
function diffrenceInDays(oldDate, newDate) {
	const _oldDate = new Date(
		oldDate.getFullYear(),
		oldDate.getMonth(),
		oldDate.getDate()
	);
	const _newDate = new Date(
		newDate.getFullYear(),
		newDate.getMonth(),
		newDate.getDate()
	);

	return Math.floor(Math.abs(_oldDate - _newDate) / 86400000);
}
